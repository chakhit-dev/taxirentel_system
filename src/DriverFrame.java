    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        QueryModule qm = new QueryModule();
        
        String dv_id = jTextField1.getText().toString();
        String dv_name = jTextField2.getText().toString();
        String dv_address = jTextField3.getText().toString();
        String dvphone = jTextField4.getText().toString();
        
        int strtoint = Integer.valueOf(dvphone);
        
        Driver dv = new Driver(dv_id, dv_name, dv_address, strtoint);
        
        int status = qm.insertDriver(dv);
        
        if(status == 1){
            loadDriverInformation();
            loadComboDriver();
            JOptionPane.showMessageDialog(this, "Driver added successfully!");
        }
        else {
            loadDriverInformation();
            loadComboDriver();
            JOptionPane.showMessageDialog(this, "Failed to added Driver !");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

