    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        QueryModule qm = new QueryModule();
        
        String getData = (String) jComboBox1.getSelectedItem();
        
        Driver status = qm.getDriverById(getData);
        
        if (status != null){
            jTextField1.setText(status.getDriverID());
            jTextField2.setText(status.getName());
            jTextField3.setText(status.getAddress());  
            String phone = String.valueOf(status.getPhone());
            jTextField4.setText(phone);
        }
        else {
            JOptionPane.showMessageDialog(this, "Data not found!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        QueryModule qm = new QueryModule();
        
        String getDriverID = (String) jComboBox1.getSelectedItem();
        
        String new_dv_id = jTextField1.getText().toString();
        String new_dv_name = jTextField2.getText().toString();
        String new_dv_address = jTextField3.getText().toString();
        String new_dv_phone = jTextField4.getText().toString();
   
        int strtoint = Integer.valueOf(new_dv_phone);
        
        Driver dv = new Driver(new_dv_id, new_dv_name, new_dv_address, strtoint);
        
        int status = qm.updateDriver(dv, getDriverID);
        
        if(status == 1){
            loadDriverInformation();
            loadComboDriver();
            JOptionPane.showMessageDialog(this, "Driver is updated !");
        }
        else {
            JOptionPane.showMessageDialog(this, "Failed to added Driver !");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

