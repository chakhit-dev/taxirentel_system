    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        QueryModule qm = new QueryModule();
        
        String getData = (String) jComboBox1.getSelectedItem();
      
        String[] times = getData.split(" - ");
        
        String startTime = times[0];
        String endTime = times[1];
        
        String new_st_start = jTextField1.getText().toString();
        String new_st_end = jTextField2.getText().toString();
        
        TimeSlot st = new TimeSlot(new_st_start, new_st_end);
        
        int status = qm.updateTimeSlot(st, startTime, endTime);
        
        if(status == 1){
            JOptionPane.showMessageDialog(this, "TimeSlot is updated !");
            loadTimeSlot();
            loadComboTimeSlot();
        }
        else {
            JOptionPane.showMessageDialog(this, "Failed to added TimeSlot !");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

