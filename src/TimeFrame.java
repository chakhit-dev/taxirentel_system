    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        QueryModule qm = new QueryModule();
        
        String getData = (String) jComboBox1.getSelectedItem();
      
        String[] times = getData.split(" - ");
        
        String startTime = times[0];
        String endTime = times[1];
        
        int status = qm.deleteTimeSlot(startTime, endTime);
        
        if(status == 1) {
            loadTimeSlot();
            loadComboTimeSlot();
            JOptionPane.showMessageDialog(this, "TimeSlot " + startTime + " " + endTime + " has deleted");
        }
        else {
            JOptionPane.showMessageDialog(this, "Failed to added TimeSlot !");
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void loadTimeSlot() {
        QueryModule qm = new QueryModule();
        List<TimeSlot> data = qm.fetchTimeSlot();
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);

        for(TimeSlot ts : data) {
            //System.out.println(dv.getDriverID());
            model.addRow(new Object[]{ts.getStartTime(), ts.getEndTime()});
            //System.out.println(dv.getDriverID() + dv.getName() + dv.getAddress() + dv.getPhone());
        }
       
        //System.out.println(timeSlots);
    }
    
